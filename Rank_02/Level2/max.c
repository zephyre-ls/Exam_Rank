int	max(int	*tab, unsigned int len)
{
	int	i;

	if (tab == 0 || len == 0)
		 return(0);
	i = 1;
	while( i < len)
	{
		if (tab[i] > tab [0])
			tab[0] = tab[i];
		i++;
	}
	return(tab[0]);
}
